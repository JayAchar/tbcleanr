#' Fix Koch 6 treatment outcomes
#'
#' Combine  treatment outcome variables to leave one 
#' factorised, labelled variable
#' @param x data frame containing outcome variables
#' @param who_defined create new factor variable with levels defined by WHO (2013)
#' @param bin_outcome create new binary factor variable simplifying treatment outcome
#' @param rm_orig remove original variables - TRUE or FALSE
#' @param ... further arguments passed to or from other methods
#' @importFrom dplyr case_when mutate %>%
#' @importFrom rlang .data
#' @author Jay Achar 
#' @seealso \code{\link{tbcleanr}}
#' @export


outcome_fixer.koch6 <- function(x, 
                                who_defined = TRUE,
                                bin_outcome = TRUE,
                                rm_orig = TRUE, 
                                ...) {
    
    # capture class of original data
    start_class <- class(x)
    
    # specify outcome vars
    # outcome variables in Koch 6
    v <- c("outfirst", "outfirst2013", "2013outcome")
    v1 <- "outfirst"
    v2 <- "outfirst2013"
    v3 <- "2013outcome"
    labs <- c("On treatment",
              "Cured",
              "Completed",
              "Death",
              "Fail",
              "LTFU",
              "Transfer out",
              "Other",
              "Transfer back to SCC")
    
    # check outcome variables present
    if (! all(v %in% names(x))) {
        stop("Check all outcome variables are included in data frame")
    }
    
    # generate final outcome variable
    x$outcome <- NA	
    
    # use all correct results from "outfirst" variable
    x$outcome[is.na(x[[v3]])] <- x[[v1]][is.na(x[[v3]])]
    
    # use all correct results from "outfirst2013" variable
    w <- which(x[[v3]] == "est 1 outcome 2013")
    x$outcome[w] <- x[[v2]][w]	
    
    # factorise outcome variable
    x$outcome <- factor(x$outcome, 
                        levels = c(0:8),
                        labels = labs)
    
    # create WHO defined outcome variable
    if (who_defined) {
        x <- x %>%
            mutate(outcome_who = case_when(outcome == "Cured" ~ "Cured",
                                           outcome == "Completed" ~ "Treatment completed",
                                           outcome == "Fail" ~ "Treatment failed",
                                           outcome == "Death" ~ "Died",
                                           outcome == "LTFU" ~ "Lost to follow-up",
                                           outcome %in% c("Transfer out", 
                                                          "Transfer back to SCC",
                                                          "Other") ~ "Not evaluated",
                                           TRUE ~ NA_character_),
                   outcome_who = factor(.data$outcome_who))
    }
    
    # create binary outcome variable 
    if (bin_outcome) {
        x <- x %>% 
            mutate(outcome_bin = case_when(outcome %in% c("Cured", "Completed") ~ "Treatment successful",
                                           outcome %in% c("Fail", "Death", "LTFU") ~ "Treatment unsuccessful",
                                           TRUE ~ NA_character_),
                   outcome_bin = factor(.data$outcome_bin))
    }
    
    # remove original variables
    if (rm_orig) {
        x[, v] <- NULL
    }
    
    class(x) <- start_class
    
    x
}
